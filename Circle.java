package assignment1;

public class Circle {

	public static void main(String[] args) {
		int r;
		double pie,Area,Perimeter;
		r = 10;
		pie = 3.14;
        Area= pie * r * r;
        Perimeter = 2 * pie * r;
        System.out.println("Area of circle :" +Area +"\n" +
                           "Perimeter of circle :"+Perimeter);
        
	}

}
